! Mousse Au Chocolate
~ Ingredients
- 4 Egg yolks
- 4 tbsp white sugar
- 250g Dark chocolate, molten
- Pinch of salt(optional)
- 6-8 shortbread biscuits
- 200ml heavy cream
- 1 tsp vanilla extract
~ Instructions
- Whip egg yolk together with sugar in a double boiler. Add salt and dark chocolate in small portions.
- Crumble biscuits and distribute evenly on four small bowls
- Add chocolate mixture on top
- Whip cream together with the vanilla extract and add on top of chocolate mixture
- Chill in fridge for at least 3 hours