! Brownies
~ Ingredients
- 150g ground almonds
- A pinch of salt
- 70g flour
- 75g brown sugar
- 75g white sugar
- 15g baking powder
- 100g butter
- 2 eggs
- 75g milk chocolate
- 75g dark chocolate
~ Steps
- Preheat oven to 180'C
- Combine flour, sugars and baking powder thoroughly
- Melt or soften butter and add to the dough, along with eggs.
- Melt both chocolates in double boiler, add to mix in double boiler stir into dough. Bake for 20 minutes on middle tray.
