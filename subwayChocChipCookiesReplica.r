! Subway-like Choc Chip Cookies
~ Ingredients
- 350g brown sugar
- 400g white sugar
- 450g butter
- 4 eggs
- 1 tsp vanilla extract
- 650g flour
- 7g baking powder
- 600g milk chocolate drops
~ Instructions
- Preheat oven to 190'C
- Melt butter, combine sugars, butter, eggs and vanilla extract
- Add flour, baking powder and milk chocolate chips to it and knead well.
- Use ice cream scoop to portion the cookies on a baking tray with parchment paper
- Bake for 12 minutes